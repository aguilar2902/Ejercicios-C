int areaCuadrado(int lado)
{
    return lado * lado;
}

int areaRectangulo(int base, int altura)
{
    return base * altura;
}

float areaCirculo(int radio)
{
    float PI = 3.14159265358979323846;

    return PI * radio * radio;
}

int areaTriangulo(int base, int altura)
{
    return (base * altura) / 2;
}